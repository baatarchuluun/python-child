# 2. Өгөгдсөн натурал тоонд 0 ба 9-ийн цифрийн аль нь олон байгааг олох програм зохио
n = int(input("n:"))
s0 = 0
s9 = 0

while n > 0:
    r = n % 10
    if r == 0: s0 += 1
    elif r == 9: s9 += 1
    n = n // 10

if s0 > s9:
    print("0 цифр олон")
elif s0 == s9:
    print("0 ба 9 цифр тэнцүү")
else:
    print("9 цифр олон")