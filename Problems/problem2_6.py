# 6. Өөрөөсөө ялгаатай бүх хуваагчийнхаа нийлбэртэй тэнцүү тоог төгс тоо гэдэг. 
# Жишээ нь 6 = 1 + 2 + 3,  28 = 1 + 2 + 4 + 7 + 14 учир 6 ба 28 нь төгс тоо болдог. 
# Өгөгдсөн тоо нь төгс мөн эсэхийг шалга. 
n = int(input("a = "))

s = 0
for i in range(1, n):
    if n % i == 0:
        s += i

if s == n:
    print("Perfect number")
else:
    print("Not perfect number")