# draw square in Python Turtle
import turtle
  
t = turtle.Turtle()
 
s = 200
 
t.forward(s) # Forward turtle by s units
t.left(90) # Turn turtle by 90 degree
 
t.forward(s) # Forward turtle by s units
t.left(90) # Turn turtle by 90 degree
 
t.forward(s) # Forward turtle by s units
t.left(90) # Turn turtle by 90 degree
 
t.forward(s) # Forward turtle by s units
t.left(90) # Turn turtle by 90 degree

turtle.done()