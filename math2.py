import math
a = 5.34
# дээшээ бүхэлдэх
print(math.ceil(a))
# доошоо бүхэлдэх
print(math.floor(a))
# үлдэгдэл
print(math.remainder(10, 3))
# язгуур авах
print(math.sqrt(25))
# factorial
print(math.factorial(6))
# зэрэгт дэвшүүлэх
print(math.pow(5, 3))
# үлдэгдэл
print(math.fmod(10, 3))
# ХИЕХ - greatest common division
print(math.gcd(16, 12))
# pi too
print(math.pi)
# euler-iin too
print(math.e)
# trigonometer
print(math.sin(0.00))
print(math.cos(0.00))