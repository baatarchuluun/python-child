import math
a = 5.34
# дээшээ бүхэлдэх
print(math.ceil(a))
# доошоо бүхэлдэх
print(math.floor(a))
# үлдэгдэл
print(math.remainder(10.5, 3))
# язгуур авах
print(math.sqrt(16))
# factorial
print(math.factorial(6))
# зэрэгт дэвшүүлэх
print(math.pow(3, 4))
# module авах
print(math.fmod(12, 5))
# ХИЕХ - greatest common division
print(math.gcd(80, 65))
# pi too
print(math.pi)
# cos, sin
print(math.sin(0.00))
print(math.cos(0.00))
# Euler-ийн тоо
print(math.e)
# exponential 
print(math.exp(3))